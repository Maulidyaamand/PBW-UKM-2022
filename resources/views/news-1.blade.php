					<div class="post-content post-excerpt">
						<p>UKM Pecinta Alam “LEUSER” Universitas Syiah Kuala (USK) Aceh menggelar kegiatan seminar daring (webinar) dalam rangka memperingati Hari Lingkungan Hidup yang jatuh pada tanggal 5 Juni. Tema webinar ini yaitu “Dampak Covid-19 terhadap Lingkungan Hidup” Selasa, (15/06/2021).

                            Acara dibuka oleh Wakil Rektor III Bidang Kemahasiswaan dan Alumni, Bapak Dr. Ir Alfiansyah Yulianur BC. Beliau berpesan untuk selalu menjadi mahasiswa bermental pejuang, di tengah keterbatasan kegiatan akibat pandemi Covid-19.</p>
						
						<p>“Saya yakin, kalian anak-anak muda bisa mengatasi masalah, 
                            menghadapi tantangan menjadi pribadi kreatif. Kalian anak 
                            muda masih kuat imunitasnya, sangat jarang kasus Covid terjadi 
                            pada anak muda, karena antibodi kalian masih bagus, maka harus dijaga 
                            dan selalu mematuhi protokol kesehatan seperti memakai masker, cuci tangan dan jaga jarak. Apalagi penggunaan masker juga memakai bahan yang sulit diurai, kita bisa diskusikan hal ini bagaimana solusinya pada kajian siang ini” ungkapnya.</p>
                            
                            <blockquote class="quote-post">
                                <p>
                                    “Pecinta alam tidak harus yang selalu masuk keluar hutan dan arung jeram, tapi juga tentu harus menjaga kelestarian alam, terutama di kota yang tak luput dari masalah sampah. Bagaimana kita edukasi masyarakat, bagaimana menyediakan tempat tapi masyarakat tidak membuang sampah pada tempatnya. Edukasi dan penyediaan fasilitas juga perlu diperhatikan”
                                </p>
                            </blockquote>

						<p>Kegiatan yang berlangsung selama kurang lebih berdurasi dua jam ini dimoderatori oleh Bapak Dr. Ir. Akhyar ST, MP, M.Eng selaku dosen pendamping UKM PA “Leuser” USK serta menghadirkan empat narasumber, pertama, Joni ST., MT., Ph.D, Kepala Bidang Tata Lingkungan dan Pengendalian Pencemaran Dinas Lingkungan Hidup dan Kehutanan (DLHK) Aceh, membahas tentang tatanan perubahan perilaku saat terjadi pandemik Covid-19, dimana sampah medis masih menduduki posisi pertama jenis sampah di tengah pandemi saat ini.</p>
                        <P>“Tentu harus dilakukan pemilahan sampah B3 dan non-B3 atau sampah domestik. Sampah limbah B3 yang pada umumnya terdapat di limbah medis Covid-19 ini juga harus dikemas dengan cara tertentu. Kita bisa mengemas dan menitipkan ke fasyankes yang umumnya memiliki peralatan penguraian sampah yang terstandar. Dan perlu menjadi catatan bersama, bahwa Covid berakhir bila kita mampu mengelola sampah dengan baik” ujarnya</P>
                        <P>Narasumber kedua yaitu Dr. T. Maulana, SKM, M.Kes, sebagai Kepala Seksi Kesehatan Lingkungan Dinas Kesehatan Aceh) membawakan materi tentang Penanganan Limbah Covid-19. Beliau menyatakan bahwa Covid-19 adalah bencana kesehatan (health disaster) dan harus dicegah bersama dengan tetap menerapkan protokol kesehatan.

                            “Ini merupakan health disaster. Sebaiknya masker dirobek-robek setelah pemakaian guna mengurangi resiko penyebaran virus Covid-19 dan tidak digunakan berulang kali, khususnya yang hanya bisa digunakan sekali. Masker untuk publik boleh yang berbahan kain yang dijual bebas di supermarket”ujarnya.</P>
                        <P>Maulana juga sempat membahas mengapa tenaga kesehatan sering terpapar virus korona. “Mengapa tenaga medis terpapar? Karena penggantian baju hazard dan alat pelindung diri (APD) lainnya, caranya terlupakan, meski beberapa rumah sakit sudah memiliki sistem cara pemakaian APD secara baik dan benar dan pengolahan sampah penanganan covid-19” tutupnya.

                            Narasumber ketiga, Muhammad Nur S.H, selaku Direktur Eksekutif WALHI Aceh. Ia sempat menyatakan kekecewaannya karena narasumber sebelumnya tidak membahas  akar masalah tentang semakin habisnya hutan dijarah oleh oknum tidak bertanggungjawab meskipun dalam kondisi pandemi.</P>
                        <P>Bang M Nur-panggilan akrabnya-menyampaikan pembahasan tentang polusi udara dan kerusakan hutan yang meskipun terjadi pandemi, tetapi tetap tidak mengubah indeks kualitas udara lebih sehat dan hal itu sama seperti sebelum adanya pandemi. PLTU tetap beroperasi. Ia juga menyinggung perihal limbah B3 yang saat ini dianggap tidak berbahaya oleh ketetapan pemerintah.  Perusahaan kelapa sawit tetap beroperasi walau pandemi. Kebakaran lahan masih terus terjadi.

                            “Saya kira DLHK punya alat untuk mengukur polusi. Tak ada laporan ke publik tentang jumlah bahan berbahaya yang keluar masuk Aceh. Pandemi hanya menumpulkan daya kritis kita dalam melihat pencemaran udara karena dengan lahirnya UU Cipta Kerja, kita sudah kehilangan daya kritis" tegasnya.</P>
                        <P>Narasumber terakhir, Lukmanul Hakim S.T, GIS Assisten dari Yayasan Hutan, alam dan Lingkungan Aceh atau HaKA. Beliau membuka kajian tentang tentang deforestasi yang terjadi di hutan Aceh. Temuannya dan tim hampir sama dengan pernyataan dari narasumber WALHI Aceh, bahwa perambahan hutan malah makin menjadi-jadi di tengah pandemic Covid-19. Dan ia bersama rekannya terus memantau persebaran lokasi deforestasi di Aceh.

                            Ditambahkan Ketua UKM PA Leuser Universitas Syiah Kuala, mengungkapkan harapannya setelah usai kegiatan ini kita semua lebih meningkatkan lagi kesadaran diri masing-masing. “Pada momen ini kita dituntut untuk lebih meningkatkan kesadaran terhadap isu lingkungan yg sedang dihadapi seperti polusi udara, polusi plastik, perdagangan satwa liar dan lainnya. Semoga hadirnya kegiatan Webinar ini bisa menjadi motivasi bagi kita semua untuk lebih peka dan adaptif dalam menjaga dan mengelola lingkungan kedepannya”tutupnya.</P>
                        
